		<p>Phoenix is a hot spot for Latin food, but you can still find a dish from any community. There are several different types of cuisines to experience here – Italian, Asian Fusion, Japanese, American Fare, Spanish and Mexican cuisine, and many others. The southwestern aromas of the dessert influences of Mexico, Native Americans, and Sonora are sure to stir your buds. </p>

